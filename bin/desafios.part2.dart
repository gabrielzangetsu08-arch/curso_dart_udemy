void main() {
  int dia = 0;

  switch (dia) {
    case 1:
      print("Segunda-feira");
      break;
    case 2:
      print("Terça-feira");
      break;
    case 3:
      print("Quarta-feira");
      break;
    case 4:
      print("Quinta-feira");
      break;
    case 5:
      print("Sexta-feira");
      break;
    case 6:
    case 7:
      print("Final de semana");
      break;
    default:
      print("Dia inválido");
  }
}
